# Data Types
# Built-in 
# type() ==> to define what type of variable
# Text Type: str
x = 'python'
# Numeric Types: int, float, complex
y = 5
y = 2.5
y = 1j
# Sequence Types: list, tuple, range
x = ['apple', 'banana', 'orange']
x = ('apple', 'banana', 'orange')
x = range(5)
# Mapping Types: dict
x = {'name': 'amr', 'age': 30}
# Set Types: set, frozenset
x = {'apple', 'banana', 'orange'}
